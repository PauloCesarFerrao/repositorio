    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        // TODO add your handling code here:
        
         
            contato obj = new contato();
            obj.setId(Integer.parseInt(campoid.getText()) );
            obj.setNome(camponome.getText());
            obj.setTelefone(Integer.parseInt(campotelefone.getText()));
            obj.setTipo(boxTipo.getSelectedItem().toString());
            obj.setId(Integer.parseInt(campoid.getText()) );
        
            try {
                dao.editar(obj);
                atualizaTabela(evt);
            } catch (SQLException ex) {
                Logger.getLogger(contatoTela.class.getName()).log(Level.SEVERE, null, ex);
            }
           
           
            
    }//GEN-LAST:event_editarActionPerformed

